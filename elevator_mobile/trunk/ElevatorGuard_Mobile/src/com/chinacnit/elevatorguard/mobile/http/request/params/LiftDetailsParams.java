package com.chinacnit.elevatorguard.mobile.http.request.params;

import com.chinacnit.elevatorguard.mobile.http.request.HttpBaseRequestParams;

/**
 * 获取电梯详情请求参数
 * 
 * @author: pyyang
 * @date 创建时间：2015年6月11日 下午6:15:06
 */
public class LiftDetailsParams extends HttpBaseRequestParams {
	/**
	 * 电梯Id
	 */
	private long liftId;

	
	public LiftDetailsParams(long liftId) {
		super();
		this.liftId = liftId;
	}

	public long getLiftId() {
		return liftId;
	}

	public void setLiftId(long liftId) {
		this.liftId = liftId;
	}
	

}
