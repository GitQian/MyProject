package com.chinacnit.elevatorguard.mobile.http.request.params;

import com.chinacnit.elevatorguard.mobile.http.request.HttpBaseRequestParams;

/**
 * 登录参数
 * 
 * @author ssu
 * @date 2015-6-1 下午7:54:50
 */
public class LoginParams extends HttpBaseRequestParams {

	/** 用户名 */
	private String userName;

	/** 用户密码 */
	private String password;

	/** mac地址 */
	private String mac;

	public LoginParams() {
		super();

	}

	public LoginParams(String userName, String password, String mac) {
		super();
		this.userName = userName;
		this.password = password;
		this.mac = mac;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
