package com.chinacnit.elevatorguard.mobile.http.request.params;

import com.chinacnit.elevatorguard.mobile.http.request.HttpBaseRequestParams;

/**
 * 验证参数
 * 
 * @author ssu
 * @date 2015-6-2 下午12:28:29
 */
public class VerificationParams extends HttpBaseRequestParams {

	private long uid;

	private long sid;

	public VerificationParams() {
		super();
	}

	public VerificationParams(long uId, long sId) {
		super();
		this.uid = uId;
		this.sid = sId;
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public long getSid() {
		return sid;
	}

	public void setSid(long sid) {
		this.sid = sid;
	}

}
